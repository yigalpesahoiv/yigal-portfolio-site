function setLanguage(lang) {
    if (!translations[lang]) return;

    // Update Text Content
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[lang][key]) {
            // Handle Placeholders (for inputs)
            if (element.hasAttribute('placeholder')) {
                element.placeholder = translations[lang][key];
            }
            // Handle Dropdown Options (value is usually fixed, but if text content needs changing)
            else if (element.tagName === 'OPTION') {
                element.textContent = translations[lang][key];
            }
            else if (['P', 'DIV', 'SPAN', 'H1', 'H2', 'H3', 'BUTTON', 'A', 'LABEL'].includes(element.tagName)) {
                // Check if contains HTML (like <br> or <span>)
                if (translations[lang][key].includes('<')) {
                    element.innerHTML = translations[lang][key];
                } else {
                    element.textContent = translations[lang][key];
                }
            } else {
                element.textContent = translations[lang][key];
            }
        }
    });

    // Update Direction and Font
    const html = document.documentElement;
    const isRTL = lang === 'he';
    html.setAttribute('lang', lang);
    html.setAttribute('dir', isRTL ? 'rtl' : 'ltr');

    // Update Active State of Buttons
    document.querySelectorAll('[data-lang-btn]').forEach(btn => {
        if (btn.getAttribute('data-lang-btn') === lang) {
            btn.classList.add('opacity-100', 'scale-110');
            btn.classList.remove('opacity-50');
        } else {
            btn.classList.add('opacity-50');
            btn.classList.remove('opacity-100', 'scale-110');
        }
    });

    // Save Preference
    localStorage.setItem('preferred_language', lang);
}

document.addEventListener('DOMContentLoaded', () => {
    // Check saved preference or browser default
    const savedLang = localStorage.getItem('preferred_language');
    const browserLang = navigator.language.startsWith('he') ? 'he' : 'en';
    const initialLang = savedLang || browserLang;

    setLanguage(initialLang);

    // Bind Click Events
    document.querySelectorAll('[data-lang-btn]').forEach(btn => {
        btn.addEventListener('click', () => {
            setLanguage(btn.getAttribute('data-lang-btn'));
        });
    });
});
